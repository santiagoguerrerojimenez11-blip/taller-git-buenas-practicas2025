def suma():
    print("Operación: Suma")
    numero1 = int(input("Ingrese el primer número: "))
    numero2 = int(input("Ingrese el segundo número: "))
    return numero1 + numero2

print(suma())



def division():
    print("Operación: División")
    numero1 = int(input("Ingrese el primer número: "))
    numero2 = int(input("Ingrese el segundo número: "))
    return numero1 / numero2

print(division())

def resta():
    print("Operación: Resta")
    numero1 = int(input("Ingrese el primer número: "))
    numero2 = int(input("Ingrese el segundo número: "))
    return numero1 - numero2

print(resta())


